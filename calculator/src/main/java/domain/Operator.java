package domain;

public class Operator {

    public static int plus(int val, int val2) {
        return val + val2;
    }

    public static int minus(int val, int val2) {
        return val - val2;
    }

    public static int divide(int val, int val2) {
        return val / val2;
    }

    public static int multiply(int val, int val2) {
        return val * val2;
    }
}
